export function applyCustomColors(theme, params) {
  const t = { ...theme };
  const colorMap = {
    bg_c: 'bg',
    card_c: 'card',
    border_c: 'card_border',
    title_c: 'title',
    text_c: 'text',
    value_c: 'value',
    icon_c: 'icon',
    accent_c: 'accent',
    star_c: 'star',
    ring_c: 'ring',
  };
  for (const [param, key] of Object.entries(colorMap)) {
    if (params[param]) t[key] = params[param];
  }
  return t;
}
